let EXAMPLE_NUMBER = 3;
console.log(EXAMPLE_NUMBER);  //3
console.log(typeof EXAMPLE_NUMBER); //number

let EXAMPLE_STRING = "this is a string data type";
console.log(EXAMPLE_STRING);
console.log(typeof EXAMPLE_STRING); //string

let boolean_value = true;
console.log("boolean_value =", boolean_value);
console.log(typeof boolean_value);

let null_value = null;
console.log("null_value =", null_value);
console.log(typeof null_value);

let something_undefined;
console.log(something_undefined); //->undefined
console.log(typeof something_undefined); //->undefined
